    <div class="blog_text">
        It’s been a few years since I played the Sims, and I remember loving being able to build what I wanted, the way I wanted it. And some part of me is hyped for Facebook’s latest innovation, the Metaverse.
        Part of me is skeptical, that maybe this is just another cash and grab scheme, ready to become the next platform for corporate to advertise on, and an endless barrage of advertisements, and some sort of premium feature that I’ll have to subscribe to in order to fully enjoy this universe.

        But, part of me is hopeful that this won’t be case. If there is even a slight possibility that we’ll be experiencing a platform that truly allows artists to express themselves, for netizens to truly mingle with one another, then I’m all for it.

        And that isn’t all. The Metaverse is also becoming exceedingly popular since the scope for NFTs is endless in this universe. NFTs are digital assets that have ownership rights, meaning you can own a unique piece of well, anything, and it will have you ownership signature on it, backed by block chain technology.

        Decentraland is one of the digital worlds in the Metaverse, and a company recently bought digital “land” in this universe for about 618,000 MANA (the digital currency used in Decentraland). The value of this transaction was roughly worth around $3 million USD. That’s something to be noted.

        Businesses (especially marketing professionals) should understand the scope of the Metaverse, since it could well be the next biggest social platform. We’ve seen how user generated content is widely accepted, and this can be coupled with the surge in interest in NFTs to create a world where you can own unique things, live your second life, and have fun doing it.

        This is just an inkling of what the Metaverse could be. We’re talking about digital worlds on the level that has only been talked about in science fiction novels, and seen in some notable movies. Artists can have a platform where anything from personalized t shirts to extremely detailed homes, buildings could fly, and different planets would be one teleportation away.

        And all you’d need for this experience is the appropriate Virtual Reality equipment. And we’ve seen massive leaps in VR tech over the last few years, with the Oculus Rift 2 being one of the recent innovation by Facebook to provide an economical virtual experience. Xiaomi is another company working to introduce a better version of Google Glasses, and I wouldn’t be surprised if a pair of glasses let you switch between the real world and the Digiworld in the click of a button.

        Picture a mixture of No Man’s sky, Bioshock, CyberPunk 2077 all rolled into one. That’s what I envision the Metaverse to be. I know that sounds extremely idealistic, but a world where our only limit is our imagination, is a world I’d be willing to be a part of.
